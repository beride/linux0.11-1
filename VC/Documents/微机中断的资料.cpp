// IRQ7并行口1（一般用作打印机）
// IRQ8实时时钟（系统保留） 
// IRQ9可用　
// IRQ10可用
// IRQ11常用于显示卡　
// IRQ12 PS/2　mouse（可用）
// IRQ13数学协处理器
// IRQ14 IDE1控制器通道　
// IRQ15 IDE2控制器通道（可用）
